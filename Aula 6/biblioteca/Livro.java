public class Livro {
    private String titulo;
    private String autor;
    private String editora;
    private String edicao;

    public Livro(String titulo, String autor, String editora, String edicao) {
        this.titulo = titulo;
        this.autor = autor;
        this.editora = editora;
        this.edicao = edicao;
    }
}